            <li>เพิ่มข้อมูลรายละเอียดของโครงการ <!-- (หากต้องการเป็น Verified Project ควรกรอกข้อมูลให้ครบทุกช่อง) --> </li>
        
    
        
        <div class="guide-ex">
            <h5 class="margin__top10">ตัวอย่างโปรเจกต์ <a href="/howtoproject" target="_blank">The Guidelight</a><!--  (ลิ้งค์ไปหน้าโปรเจกต์ The Guidelight) -->      </h5>
        1. ชื่อโครงการ : ควรตั้งชื่อให้กระชับ จดจำได้ง่าย สื่อความหมาย และมีความเกี่ยวข้องกับโปรเจกต์ <br>
        2. ภาพประกอบโครงการ : สามารถอัพโหลดรูปภาพ โดยอาจเป็นโลโก้ / รูปทีมงาน หรือรูปภาพที่เกี่ยวข้อง ขนาด  650 x 370 pixel <br>
        3.แนวคิดโครงการ : อธิบายแนวคิดโดยรวมว่า ที่มาของโครงการ มีวัตถุประสงค์อะไร ดำเนินการในรูปแบบใด <br>
        4. ระดับขั้นของโครงการ : ระบุขั้นของการดำเนินการโครงการว่าอยู่ในขั้นใด เช่น ขั้นคิดและวางแผน ขั้นที่กำลังลงมือทำแล้ว หรือขั้นการขยายผลโครงการ<br>
        5. วันเริ่มโครงการ : ระบุวันเดือนปีที่เริ่มโครงการ<br>
        6. พื้นที่ที่ทำโครงการ : ระบุพื้นที่ที่โครงการเลือกที่จะไปทำงานด้วยให้ชัดเจน<br>
        7. ประเด็นปัญหา : ระบุประเด็นปัญหาสังคมที่ทางโครงการเลือกที่จะลงมือแก้ไข<br>
        8. ปัญหา : อธิบายถึงสถานการณ์ของประเด็นปัญหานั้นๆ พร้อมเล่าสาเหตุหลักที่ทำให้เกิดปัญหา<br>
        9. กลุ่มเป้าหมาย : ระบุกลุ่มเป้าหมายที่โครงการทำงานด้วย หรือได้รับผลกระทบจากโครงการ<br>
        10.  เป้าหมายของโครงการ : ระบุเป้าหมายระยะสั้น และระยะยาวของโครงการให้ชัดเจน<br>
        11. วิธีการแก้ไข : ระบุวิธีที่ทางโครงการเลือกที่จะใช้ป้องกัน หรือแก้ไขปัญหาสังคมดังกล่าว<br>
        12. ผลกระทบทางสังคม : อธิบายผลที่คาดว่าจะเกิดขึ้นในทางสังคมหากทำโครงการนี้สำเร็จ <br>
        13. ที่มาของโครงการ : อธิบายเหตุผลหรือแรงบันดาลใจในการริเริ่มโครงการ และโครงการนี้มีความเชื่อมโยงกับทีมงานที่คิดริเริ่มโครงการอย่างไรบ้าง<br>
