        <h4 class="margin__top30">เงื่อนไข และขั้นตอนการ Verify Project </h4>
        <ol class="margin__top10">
            <li>โครงการที่จะได้รับการพิจารณาให้เป็น Verified Project นั้น ผู้สร้างโครงการ จะต้อง ยืนยันตัวตนด้วยเบอร์โทรศัพท์ เพื่อเป็น Verified Member ก่อน และกรอกข้อมูลโครงการได้ครบถ้วนและถูกต้อง</li>
            
            <li>ทีมงานจะพิจารณาเป็นรายสัปดาห์ ซึ่งสมาชิกสามารถตรวจสอบได้ที่หน้า Project ของคุณ บริเวณหัวข้อ Verification หากโครงการผ่านการพิจารณาแล้ว จะปรากฎข้อความว่า Verified </li>
            
            <li>ประโยชน์ที่ได้รับจากการเป็น Verified Project คือ จะได้รับสิทธิพิเศษในการเข้าร่วมกิจกรรม หรือเข้ารับการสนับสนุนก่อนโครงการทั่วๆ ไป ดังนี้</li>
            
            <ul style="list-style: initial;">
                <li>กิจกรรม Classroom A-B-C เพื่อพัฒนาไอเดีย และวางแผนการทำโครงการ</li>
                <li>การขอรับคำปรึกษาจากผู้เชี่ยวชาญในกิจกรรม HelpDesk</li>
                <li>การขอรับการสนับสนุนโดยโค้ช เพื่อให้คำแนะนำในการทำโครงการ เป็นระยะเวลา 6 เดือน (ระยะเวลาของการโค้ช จะมีการพิจารณาเป็นรายโครงการอีกครั้ง)</li>
                <li>ทีมงานช่วยดูแลและดำเนินการประชาสัมพันธ์ Need และ Offer ที่โครงการได้สร้างขึ้น</li>
                <li>เข้าร่วมแคมเปญที่อาจจัดขึ้นเฉพาะสำหรับ Verified Project เท่านั้น</li>
            </ul>
            
            
            <li  class="margin__top20" >โครงการที่ผ่านการพิจารณาเป็น Verified Project แล้ว อาจถูกเพิกถอนสิทธิ์ได้ โดยทีมงานไม่ต้องแจ้งให้ทราบก่อนล่วงหน้า หากพบเบาะแส หรือหลักฐานเบื้องต้นที่แสดงถึงความไม่เหมาะสม ทั้งทางด้านพฤตินัยและนิตินัย ของสมาชิกในทีม และ/หรือ การดำเนินการของโครงการ</li>
                
            <li>สำหรับโครงการที่ยังไม่ผ่านการ Verify ยังคงสามารถใช้งานส่วนอื่นๆ ของเว็บไซต์ School of Changemakers ได้ตามปกติ โดยคุณสามารถพัฒนาโครงการให้มีข้อมูลที่ชัดเจนขึ้น ด้วยเครื่องมือต่างๆ ดังนี้ 
            </li>
            
            <ul style="list-style: initial;">
                <li>เข้าร่วม กิจกรรม <a href="/event-and-workshop">Classroom</a> ที่จัดขึ้นเป็นประจำทุกเดือน</li>
                <li>ดาวน์โหลด <a target="_blank" href="/cmtoolkit.pdf">Changemakers’ Toolkit</a> เพื่อพัฒนาไอเดีย ที่นำไปสู่การทำโครงการ </li>
            </ul>
            
            
        </ol>
        
        
