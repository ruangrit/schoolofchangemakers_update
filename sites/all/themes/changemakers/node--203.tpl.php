        <p class="margin__top10 txt__indent30">สำหรับ Changemaker ที่สนใจ “เริ่มต้น” ทำโครงการเพื่อสังคม (Social Project) หรือกิจการเพื่อสังคม (Social Enterprise)  สามารถสร้างหน้าโครงการ (Project) บนเว็บไซต์ School of Changemakers  เพื่อ ใช้แทนหน้าเว็บไซต์ของโครงการ บอกเล่ารายละเอียดของโครงการ แสดงผลความคืบหน้าของโครงการในการสื่อสารกับทีม พาร์ทเนอร์และคนทั่วไป   รวมไปถึงสามารถใช้งานบริการอื่นๆ บนเว็บไซต์ School of Changemakers เพื่อสนับสนุนการดำเนินงานโครงการได้อีกด้วย   
        </p> 
        <p class="margin__top10 txt__indent30">  
        Functions สำคัญๆ ของ Project มีดังนี้
        </p>

        <ul class="margin__top10">
            <li>เพิ่มเพื่อนร่วมทีมเข้ามาในโครงการ </li>
            <li>สามารถอัพเดทความคืบหน้าโครงการ (Project Update) โดยเจ้าของโครงการ (Project Owner) หรือ ทีมงานได้ และแชร์ผ่าน Social Media เช่น Facebook ได้ทันที</li>
            <li>อัพเดทประสบการณ์ และสิ่งที่ได้เรียนรู้จากการทำโครงการ หรือความรู้ที่เกี่ยวข้องกับโครงการที่ทำได้ ใน Journal  </li>
            <li>ผู้ที่สนใจและเกี่ยวข้องกับโครงการของเรา สามารถติดตามความเคลื่อนไหวได้ ด้วยการกด Follow Project </li>
            <li>สร้าง Need & Offer ของโครงการใน Community Webboard ได้ </li>
            <li>ส่งโปรเจกต์เข้าร่วมแคมเปญต่างๆ เพื่อรับการสนับสนุนด้านเงินทุน และทรัพยากรอื่นๆ (ขึ้นอยู่กับเงื่อนไขของแต่ละแคมเปญ) </li>
            <li>สามารถจัดเก็บไฟล์เอกสารที่เกี่ยวข้องกับโครงการ ได้ที่หน้า Document ซึ่งสามารถทำให้โค้ช หรือที่ปรึกษา (หากมี) สามารถติดตามความคืบหน้างานโครงการได้ง่ายขึ้น </li>

        </ul>

        
