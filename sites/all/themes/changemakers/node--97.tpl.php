<p class="margin__top10 txt__indent30">
การริเร่ิมโครงการหรือกิจการเพื่อสังคม เป็นความสนใจของคนรุ่นใหม่ ทุกยุค ทุกสมัย ที่ “อยากทําเพื่อสังคม” มากมาย และส่วนใหญ่จะได้มีโอกาสทํางานอาสาสมัครหรือทํากิจกรรมช่วงเรียนมหาวิทยาลัย และยังมีคนอีกกลุ่มที่มองเห็นช่องว่างและ โอกาสริเริ่มสร้างสรรค์งานการแก้ปัญหาสังคมใหม่ๆ ขึ้นมาในรูปแบบของโครงการและกิจการเพื่อสังคม ซึ่งทางเราเรียก เลือดใหม่เหล่านี้ว่า Changemakers   
</p>
<p class="margin__top20 txt__indent30">        
