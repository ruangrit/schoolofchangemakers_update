        <p class="margin__top10 txt__indent30">
    นอกจากเงินทุน ความรู้ และเครือข่ายที่ School of Changemakers สนับสนุนให้กับคนรุ่นใหม่ริเริ่มและลงมือทําโครงการหรือกิจการเพื่อสังคม แล้วเรายังพยายามทํางานกับบุคคลทั่วไปที่สนใจเรื่องการสร้างการเปลี่ยนแปลงสังคม ทั้งจากภาคธุรกิจและภาคสังคม ผู้ซึ่งมีประสบการณ์ แต่มีเวลาจํากัด ไม่สามารถเริ่มลงมือทําเต็มตัวได้ จะได้เข้ามามีส่วนร่วมทํางานกับกลุ่มคนรุ่นใหม่ ใน บทบาทของการเป็น "โค้ช" เพื่อสนับสนุนการทำโครงการและกิจการเพื่อสังคมของคนรุ่นใหม่</p>

        <h4 class="margin__top30">โค้ชคือใคร?</h4>
        
        <p class="margin__top10 txt__indent30">
            โค้ช คือผู้สนับสนุนคนรุ่นใหม่ให้เลือกนําสิ่งที่ตนเองสนใจมาพัฒนาเป็นไอเดีย วางแผน และลงมือทํา จนเกิดการเรียนรู้และ เป็นนักสร้างการเปลี่ยนแปลง (Changemaker) เปรียบเสมือนกระจกท่ีจะสะท้อนให้คนรุ่นใหม่ที่สนใจอยากแก้ไขปัญหา สังคมได้เข้าใจตนเอง เข้าใจสถานการณ์ปัญหาท่ีเกิดข้ึน มีเป้าหมายชัดเจน และสามารถคิดวิธีจัดการต่างๆเพื่อให้โครงการ หรือกิจการสังคมของตนดําเนินงานได้อย่างมีประสิทธิภาพ ผ่านการรับฟังความคิด ตั้งคําถาม และป้อนข้อมูลเชิงบวกของ โค้ช มีกระบวนการดึงศักยภาพของคนรุ่นใหม่ออกมา ตลอดจนให้กําลังใจระหว่างทําโครงการ นอกจากนี้ยังรวมถึงความรู้ และประสบการณ์ของโค้ช ที่จะช่วยเสริมให้พวกเขา ฝ่าฟันอุปสรรคและบรรลุเป้าหมายท่ีตั้งไว้ จนเกิดผลกระทบท่ีดีต่อ สังคมได้อย่างเป็นรูปธรรมและเกิดประโยชน์สูงสุดท้ังต่อคนรุ่นใหม่เองและต่อสังคมรอบข้าง </p>

        <h4 class="margin__top30">คุณสมบัติโค้ช</h4>
        <ul  class="margin__top10">
            <li>เชื่อในพลังและศักยภาพของคนรุ่นใหม่ว่าสามารถเปลี่ยนแปลงสังคมได้</li>
            <li>สนใจสนับสนุนคนรุ่นใหม่ในการริเริ่มและลงมือเปลี่ยนแปลงสังคม</li>
            <li>เข้าใจบทบาทของโค้ช</li>
            <li>เปิดรับความเห็นต่าง พร้อมเปิดใจเรียนรู้ส่ิงใหม่</li>
            <li>มีประสบการณ์ในการริเริ่มลงมือทําโครงการเพื่อสังคม หรือมีประสบการณ์ในการบริหารงาน การนำทีม</li>
            <li>สามารถจัดสรรเวลาในการให้คําแนะนําโครงการของคนรุ่นใหม่ที่ตนดูแลได้อย่างน้อยสัปดาห์ละ 2 ชั่วโมง  เป็นเวลาอย่างน้อย 6 เดือน</li>
        </ul>
        
